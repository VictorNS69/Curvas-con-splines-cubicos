function DibujaCurva3D()
    P=zeros(21,3);
    P(1,:)=[0 0 0.5];
    P(2,:)=[0 1 -0.45];
    P(3,:)=[0 2 0.4];
    P(4,:)=[0 3 -0.35];
    P(5,:)=[0 4 0.3];
    P(6,:)=[0 5 -0.25];
    P(7,:)=[0 6 0.2];
    P(8,:)=[0 7 -0.15];
    P(9,:)=[0 8 0.1];
    P(10,:)=[0 9 -0.05];
    P(11,:)=[1 9 0.05];
    P(12,:)=[1 8 -0.1];
    P(13,:)=[1 7 0.1];
    P(14,:)=[1 6 -0.15];
    P(15,:)=[1 5 0.15];
    P(16,:)=[1 4 -0.2];
    P(17,:)=[1 3 0.2];
    P(18,:)=[1 2 -0.25];
    P(19,:)=[1 1 0.25];
    P(20,:)=[1 0 -0.3];
    P(21,:)= P(1,:);
    plot3(P(:,1),P(:,2),P(:,3),'ro', P(:,1),P(:,2),P(:,3),'b--');
    hold on
    % Initially, the list of points is empty.
    xyz = [];
    n = 0;
    for k=1:21
        xi = P(k,1);
        yi = P(k,2);
        zi = P(k,3);
        plot3(xi,yi,zi,'ro')
        n = n+1;
        xyz(:,n) = [xi;yi;zi];
    end
    for k=1:20
        t = 1:n;
        ts = 1: 0.1: n;
        xs = spline3(t,xyz(1,:),ts);  
        ys = spline3(t,xyz(2,:),ts);
        zs = spline3(t,xyz(3,:),ts);
        plot3(xs,ys,zs,'b-')
    end
end